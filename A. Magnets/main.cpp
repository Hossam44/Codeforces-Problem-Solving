#include <iostream>

using namespace std;

int main()
{
    int n,m=1;
    cin>>n;
    char arr[n][2];
    for(int i=0;i<n;i++)
        cin>>arr[i];

    for(int i=0;i<n;i++)
    {
        if(arr[i][0]!=arr[i+1][0])
        {
            if(i+1!=n)
              m+=1;
        }
    }
    cout<<m<<endl;

    return 0;
}
